    private void btnChangePinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangePinActionPerformed
        boolean f=false;
        if(!txtPin.getText().equals(txtRPin.getText()))
        JOptionPane.showMessageDialog(rootPane, "Re-typed password doesn't match!!");
        else{
            try {
                Registry reg = LocateRegistry.getRegistry("localhost",1086);
                ATMRMI_Interface j =(ATMRMI_Interface)reg.lookup("enter");
                f=j.pinChange(txtRPin.getText(),a,p);
                if(f){
                JOptionPane.showMessageDialog(rootPane, "PIN changed successfully!!");
                }else{
                JOptionPane.showMessageDialog(rootPane, "PIN change unsuccessful!!");
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
            }
                }
    }//GEN-LAST:event_btnChangePinActionPerformed

