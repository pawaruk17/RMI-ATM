    private void btnWithdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnWithdrawActionPerformed
        int bal,amt;
        try {
                Registry reg = LocateRegistry.getRegistry("localhost",1086);
                ATMRMI_Interface j =(ATMRMI_Interface)reg.lookup("enter");
                bal=j.checkBalance(a,p);
                amt=Integer.parseInt(txtWithdraw.getText());
                if(amt>=bal){
                JOptionPane.showMessageDialog(rootPane, "Insufficient balance "+bal);
                }else{
                    bal=j.withdraw(txtWithdraw.getText(),a,p);
                JOptionPane.showMessageDialog(rootPane, "Deposited sucessfully current balance is "+bal);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(rootPane, ex);
            }
    }//GEN-LAST:event_btnWithdrawActionPerformed

